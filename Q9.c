//Write a program to print size of an int, a float, a char and a double type variable.
#include<stdio.h>
int main()
{
   printf("The Size of int  type variable is %d\n",sizeof(int));
   printf("The Size of char type variable is %d\n",sizeof(char));
   printf("The Size of float type variable is %d\n",sizeof(float));
   printf("The Size of double type variable is %d\n",sizeof(double)); 
   return 0;
}